def add(plus,pluse)
    (plus,pluse) = plus + pluse
end

def substract(moins,moinse)
    (moins,moinse) = moins - moinse
end

def multiply(fois,foise)
    (fois,foise) = fois + foise
end

def power(puis,puise)
    (puis,puise) = puis**puise
end


